    private void btnGuardar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardar1ActionPerformed

        String razon = comboboxRazonRechazo.getSelectedItem().toString();
        int idRazon = comboboxRazonRechazo.getSelectedIndex();

        String estado = txtEstadoItem.getText();

        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();

  
        
        int row = jTable1.getSelectedRow();
        modelo.setValueAt(idRazon, row, 5);
        modelo.setValueAt(razon, row, 6);
        modelo.setValueAt(estado, row, 7);


    }//GEN-LAST:event_btnGuardar1ActionPerformed

    public void limpiarTabla() {

        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        for (int i = 0; i < jTable1.getRowCount(); i++) {
            modelo.removeRow(i);
            i = -1;
        }

    }

